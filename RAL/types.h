#ifndef RAL_TYPES_H_
#define RAL_TYPES_H_


#ifndef TRUE
#define TRUE     (1U)
#endif

#ifndef FALSE
#define FALSE    (0U)
#endif


#ifndef NULL
#define NULL ((void*) 0U)
#endif


typedef unsigned int uint32;

/** \brief create a 8 bit unsigned type */
typedef unsigned char uint8;

/** \brief create a 16 bit unsigned type */
typedef unsigned short uint16;



#endif /* RAL_TYPES_H_ */
